export function pageName(pgName) {
    if (pgName == "homeContent") {
        $("nav").removeClass("notHomePage").addClass("homePage");
    } else {
        $("nav").removeClass("homePage").addClass("notHomePage");
    }
    try {
        $("#app").html(eval(pgName));
    } catch (e) {
        console.log(e);
    }
}